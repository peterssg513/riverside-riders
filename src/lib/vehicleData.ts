// Generate year options from 1900 to 2026
export function getYearOptions(): number[] {
  const years: number[] = [];
  for (let y = 2026; y >= 1900; y--) {
    years.push(y);
  }
  return years;
}

// Fetch models for a specific make from NHTSA API
export async function fetchModelsForMake(make: string): Promise<string[]> {
  try {
    const res = await fetch(
      `https://vpic.nhtsa.dot.gov/api/vehicles/GetModelsForMake/${encodeURIComponent(
        make
      )}?format=json`
    );
    const data = await res.json();
    const models: string[] = data.Results.map(
      (r: { Model_Name: string }) => r.Model_Name
    )
      .filter((name: string) => name && name.trim().length > 0)
      .map((name: string) => name.trim())
      .sort((a: string, b: string) => a.localeCompare(b));
    return [...new Set(models)];
  } catch {
    return [];
  }
}

